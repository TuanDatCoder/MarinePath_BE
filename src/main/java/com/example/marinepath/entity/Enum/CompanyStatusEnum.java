package com.example.marinepath.entity.Enum;

public enum CompanyStatusEnum {
    ACTIVE,        // Công ty đang hoạt động bình thường
    INACTIVE,      // Công ty tạm ngừng hoạt động
    SUSPENDED,     // Công ty bị tạm đình chỉ hoạt động
    BANKRUPT,      // Công ty phá sản
    DISSOLVED,     // Công ty đã giải thể
    PENDING,       // Công ty đang chờ phê duyệt
    APPROVED,      // Công ty đã được phê duyệt hoạt động
    REJECTED,      // Công ty bị từ chối đăng ký hoạt động
    BLOCKED,        // Công ty bị chặn hoặc cấm hoạt động
    DELETED
}
